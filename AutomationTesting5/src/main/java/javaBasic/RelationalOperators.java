package javaBasic;

public class RelationalOperators {

public static void main(String[] args) {

//Relational Operators
//== , != , > , < , >= ,<=

int seven = 7;
int ten = 10;

// (firstvalue R-Operator secondvalue)
System.out.println("is " + seven + " == " + ten + " ? " + (seven == ten));//false
System.out.println(" is " + seven + " != " + ten + " ? " + ( seven != ten ));//true
System.out.println(" is " + seven + " > " + ten + " ? " + (seven > ten ));//false
System.out.println(" is " + seven + " < " + ten + " ? " + (seven < ten ));//true
System.out.println(" is " + seven + " >= " + ten + " ? " + (seven >= ten ));//false
System.out.println(" is " + seven + " > " + ten + " ? " + (seven > ten ));//false
System.out.println(" is " + seven + " <= " + ten + " ? " + (seven <= ten ));//true

	}

}
